$(document).ready(function(){
  // При наведении на слово "КОТ"
  $(".text-block p").on("mouseenter", function(){
    // Получаем текст из параграфа
    var text = $(this).text();

    // Разбиваем текст на отдельные слова
    var words = text.split(" ");

    // Проверяем каждое слово на наличие слова "кот"
    for (var i = 0; i < words.length; i++) {
      if (words[i].toLowerCase() === "кот") { // Если находим слово "кот"
        // Заменяем текст на изображение кота с мышью
        $(this).html("Рассказ про котов и мышей. Вчера на улице я видел кота. Кот подошел ко мне и попросил еды. Я погладил кота и он ушел. Вечером я снова увидел кота, но на этот раз он принес мне мышь. Этот кот очень умный!");
        $(".image-block img").attr("src", "cat.jpg");
        $(".image-block img").fadeIn();
        break; // Прерываем цикл после первого найденного слова "кот"
      }
    }
  });

  // При выходе курсора из блока
  $(".text-block p").on("mouseleave", function(){
    // Возвращаем исходный текст и изображение кота
    $(this).html("Рассказ про котов и мышей. Вчера на улице я видел кота. Кот подошел ко мне и попросил еды. Я погладил кота и он ушел. Вечером я снова увидел кота, но на этот раз он принес мне мышь. Этот кот очень умный!");
    $(".image-block img").attr("src", "cat.jpg");
    $(".image-block img").fadeOut();
  });
});
